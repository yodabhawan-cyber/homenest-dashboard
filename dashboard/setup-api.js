/**
 * HomeNest Setup API
 * Handles first-time configuration and ongoing settings management
 */
import http from 'http';
import fs from 'fs/promises';
import path from 'path';
import { exec } from 'child_process';
import { promisify } from 'util';

const execAsync = promisify(exec);

const PORT = 3201;
const BASE_DIR = path.dirname(new URL(import.meta.url).pathname);
const CONFIG_DIR = path.join(BASE_DIR, '..', 'config');
const SETUP_FILE = path.join(CONFIG_DIR, 'setup-status.json');
const ENV_FILE = path.join(BASE_DIR, '..', '.env');

// Helper to read/write JSON
async function readJSON(filepath, defaultValue = {}) {
  try {
    const data = await fs.readFile(filepath, 'utf8');
    return JSON.parse(data);
  } catch {
    return defaultValue;
  }
}

async function writeJSON(filepath, data) {
  await fs.writeFile(filepath, JSON.stringify(data, null, 2));
}

// Test OpenAI connection
async function testOpenAI(apiKey) {
  try {
    const response = await fetch('https://api.openai.com/v1/models', {
      headers: { 'Authorization': `Bearer ${apiKey}` }
    });
    
    if (response.ok) {
      return { success: true };
    } else {
      const error = await response.text();
      return { success: false, error: `API returned ${response.status}: ${error}` };
    }
  } catch (err) {
    return { success: false, error: err.message };
  }
}

// Test Home Assistant connection
async function testHomeAssistant(url, token) {
  try {
    const response = await fetch(`${url}/api/`, {
      headers: { 'Authorization': `Bearer ${token}` }
    });
    
    if (response.ok) {
      const data = await response.json();
      return { success: true, message: data.message || 'Connected' };
    } else {
      return { success: false, error: `HA returned ${response.status}` };
    }
  } catch (err) {
    return { success: false, error: err.message };
  }
}

